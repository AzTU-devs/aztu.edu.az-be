-- admin_users.id is `integer primary key` with NO identity in the schema of
-- record. POST /api/admin-users does INSERT ... RETURNING id and WILL fail on a
-- null PK until this runs. Idempotent; safe to re-run.
do $$
declare seq text;
begin
    if (select is_identity from information_schema.columns
        where table_schema='public' and table_name='admin_users' and column_name='id') = 'NO' then
        alter table admin_users alter column id drop default;
        alter table admin_users alter column id add generated by default as identity;
        seq := pg_get_serial_sequence('public.admin_users', 'id');
        perform setval(seq, coalesce((select max(id) from admin_users), 0) + 1, false);
    end if;
end $$;

